    public void HouseTypeValidation(Integer value) {
        switch (value) {
            case 1:
                ApartemanMaskoni(true);
                break;
            case 2:
                ApartemanEdari(true);
                break;
            case 3:
                Villa(false);
                break;
            case 4:
                Mostagelat(false);
                break;
            case 5:
                Colangi(false);
                break;
            case 6:
                Zemin(false);
                break;
            case 7:
                Tejari(false);
                break;
        }
    }
